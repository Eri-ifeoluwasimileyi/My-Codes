public class Prime {
    public static void main(String[] args) {
	


	for(int counter = 1; counter <= 10; counter = counter + 1){		
		int number = 0;

		if(number % 2 == 0) {
			System.out.print("This is not a prime number!!!.");

		}
		if(number <= counter/2){


		System.out.print("This is a prime number!. ");
		} else break;
		
			
	}



   }
}